use std::char::REPLACEMENT_CHARACTER;

pub fn add_html_token(result: &mut String, token: &[u8], add_space: bool) {
    if add_space {
        result.push(' ');
    }

    if let [b'&', entity @ .., b';'] = token {
        let mut entity_code: u32 = 0;
        if let [b'#', code @ ..] = entity {
            let (code, radix) = if let [b'x' | b'X', code @ ..] = code {
                (code, 16)
            } else {
                (code, 10)
            };

            entity_code = std::str::from_utf8(code)
                .map_or(0, |code| u32::from_str_radix(code, radix).unwrap_or(0));
        } else if (2..=31).contains(&entity.len()) {
            let mut hash = entity.len() as u32;

            debug_assert_eq!(ENTITY_HASH.len(), u8::MAX as usize + 5);
            debug_assert_eq!(ENTITY_MAP.len(), 18079 - 64 + 1);

            for (pos, &ch) in entity.iter().enumerate() {
                match pos {
                    0 | 5 | 6 | 9 | 11 => {
                        hash += {
                            #[cfg(feature = "ludicrous_mode")]
                            unsafe {
                                *ENTITY_HASH.get_unchecked(ch as usize)
                            }
                            #[cfg(not(feature = "ludicrous_mode"))]
                            ENTITY_HASH[ch as usize]
                        };
                    }
                    1 => {
                        hash += {
                            #[cfg(feature = "ludicrous_mode")]
                            unsafe {
                                *ENTITY_HASH.get_unchecked(ch as usize + 4)
                            }
                            #[cfg(not(feature = "ludicrous_mode"))]
                            ENTITY_HASH[ch as usize + 4]
                        };
                    }
                    2 | 4 => {
                        hash += {
                            #[cfg(feature = "ludicrous_mode")]
                            unsafe {
                                *ENTITY_HASH.get_unchecked(ch as usize + 1)
                            }
                            #[cfg(not(feature = "ludicrous_mode"))]
                            ENTITY_HASH[ch as usize + 1]
                        };
                    }
                    3 => {
                        hash += {
                            #[cfg(feature = "ludicrous_mode")]
                            unsafe {
                                *ENTITY_HASH.get_unchecked(ch as usize + 3)
                            }
                            #[cfg(not(feature = "ludicrous_mode"))]
                            ENTITY_HASH[ch as usize + 3]
                        };
                    }
                    _ => (),
                }
                if pos == entity.len() - 1 {
                    hash += {
                        #[cfg(feature = "ludicrous_mode")]
                        unsafe {
                            *ENTITY_HASH.get_unchecked(ch as usize)
                        }
                        #[cfg(not(feature = "ludicrous_mode"))]
                        ENTITY_HASH[ch as usize]
                    };
                }
            }

            if (64..=18079).contains(&hash) {
                entity_code = {
                    #[cfg(feature = "ludicrous_mode")]
                    unsafe {
                        *ENTITY_MAP.get_unchecked((hash - 64) as usize)
                    }
                    #[cfg(not(feature = "ludicrous_mode"))]
                    ENTITY_MAP[(hash - 64) as usize]
                };
            }
        }

        if entity_code > 0 {
            result.push(char::from_u32(entity_code).unwrap_or(REPLACEMENT_CHARACTER));
            return;
        }
    }

    result.push_str(std::str::from_utf8(token).unwrap());
}

pub fn html_to_text(input: &str) -> String {
    let mut result = String::with_capacity(input.len());
    let input = input.as_bytes();

    let mut in_tag = false;
    let mut in_head = false;
    let mut in_comment = false;

    let mut is_token_start = true;
    let mut is_after_space = false;
    let mut is_tag_close = false;
    let mut is_new_line = true;

    let mut token_start = 0;
    let mut token_end = 0;

    let mut tag_token_pos = 0;
    let mut comment_pos = 0;

    for (pos, ch) in input.iter().enumerate() {
        if !in_comment {
            match ch {
                b'<' => {
                    if !in_tag && !in_head && !is_token_start {
                        add_html_token(
                            &mut result,
                            &input[token_start..token_end + 1],
                            is_after_space,
                        );
                        is_after_space = false;
                    }

                    tag_token_pos = 0;
                    in_tag = true;
                    is_token_start = true;
                    is_tag_close = false;
                    continue;
                }
                b'>' if in_tag => {
                    if tag_token_pos == 1 {
                        match input.get(token_start..token_end + 1) {
                            Some(tag)
                                if tag.eq_ignore_ascii_case(b"br")
                                    || (tag.eq_ignore_ascii_case(b"p") && is_tag_close) =>
                            {
                                result.push('\n');
                                is_after_space = false;
                                is_new_line = true;
                            }
                            Some(tag) if tag.eq_ignore_ascii_case(b"head") => {
                                in_head = !is_tag_close;
                            }
                            _ => (),
                        }
                    }

                    in_tag = false;
                    is_token_start = true;

                    continue;
                }
                b'/' if in_tag => {
                    if tag_token_pos == 0 {
                        is_tag_close = true;
                    }
                    continue;
                }
                b'!' if in_tag && tag_token_pos == 0 => {
                    if let Some(b"--") = input.get(pos + 1..pos + 3) {
                        in_comment = true;
                        continue;
                    }
                }
                b' ' | b'\t' | b'\r' | b'\n' => {
                    if !in_tag && !in_head {
                        if !is_token_start {
                            add_html_token(
                                &mut result,
                                &input[token_start..token_end + 1],
                                is_after_space && !is_new_line,
                            );
                            is_new_line = false;
                        }
                        is_after_space = true;
                    }

                    is_token_start = true;

                    continue;
                }
                b'&' if !in_tag && !is_token_start && !in_head => {
                    add_html_token(
                        &mut result,
                        &input[token_start..token_end + 1],
                        is_after_space && !is_new_line,
                    );
                    is_new_line = false;
                    is_token_start = true;
                    is_after_space = false;
                }
                b';' if !in_tag && !is_token_start && !in_head => {
                    add_html_token(
                        &mut result,
                        &input[token_start..pos + 1],
                        is_after_space && !is_new_line,
                    );
                    is_token_start = true;
                    is_after_space = false;
                    is_new_line = false;
                    continue;
                }
                _ => (),
            }
            if is_token_start {
                token_start = pos;
                is_token_start = false;
                if in_tag {
                    tag_token_pos += 1;
                }
            }
            token_end = pos;
        } else {
            match ch {
                b'-' => comment_pos += 1,
                b'>' if comment_pos == 2 => {
                    comment_pos = 0;
                    in_comment = false;
                    in_tag = false;
                    is_token_start = true;
                }
                _ => comment_pos = 0,
            }
        }
    }

    if !in_tag && !is_token_start && !in_head {
        add_html_token(
            &mut result,
            &input[token_start..token_end + 1],
            is_after_space && !is_new_line,
        );
    }

    result.shrink_to_fit();
    result
}

pub fn text_to_html(input: &str) -> String {
    let input = input.as_bytes();
    let mut result = Vec::with_capacity(input.len() + 26);

    result.extend_from_slice(b"<html><body>");

    for &ch in input {
        match ch {
            b'\n' => result.extend_from_slice(b"<br/>"),
            b'<' => result.extend_from_slice(b"&lt;"),
            b'\r' => (),
            _ => result.push(ch),
        }
    }
    result.extend_from_slice(b"</body></html>");

    String::from_utf8(result).unwrap()
}

